export const render3DIcon = (icon: string, className?: string) => {
  if (icon && icon.startsWith('http')) {
    return (
      <img 
        src={icon} 
        alt="Icon" 
        className={cn("object-contain", className)} 
        referrerPolicy="no-referrer"
      />
    );
  }
  return <span className={className}>{icon}</span>;
};
